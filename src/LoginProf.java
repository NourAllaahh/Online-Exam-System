    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
          try{
            
       String query = "SELECT * FROM `professor` WHERE ProfName=? and ProfPass=? ";
       con = DriverManager.getConnection("jdbc:mysql://localhost:3306/onlineexam?zeroDateTimeBehavior=convertToNull","root","");
       pst=   (com.mysql.jdbc.PreparedStatement) con.prepareStatement(query);
       pst.setString (1, profnametxt.getText());
       pst.setString (2, passnametxt.getText());
       
         
          rs=pst.executeQuery();
       
            
           if(rs.next()){
            JOptionPane.showMessageDialog(this, "Welcome" +rs.getString("ProfName"));
            
            UsersFactory UsersFactory = new UsersFactory();
            Users Professor = UsersFactory.getUsers("Professor"); 
            Professor.login();
            
           }
             else{
                 JOptionPane.showMessageDialog(this, "Username and Password Invalid");  
        }
            
         
        } catch (SQLException ex) {
          JOptionPane.showMessageDialog(this, ex.getMessage());   
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

