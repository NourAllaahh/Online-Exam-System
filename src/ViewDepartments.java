    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
                                         
        try {
         String deptcode = deptcodetxt.getText();
            String deptname = deptnametxt.getText();
            
       
            pst = con.prepareStatement("insert into department(DeptCode,DeptName)values(?,?)");
            pst.setString(1, deptcode);
            pst.setString(2, deptname);
            
            int k =   pst.executeUpdate();
            
            if (k==1)
            {
                JOptionPane.showMessageDialog(this, "Department Added");
                
                deptcodetxt.setText("");
                deptnametxt.setText("");
          
                deptnametxt.requestFocus();
                Load();
            }
            else{
                JOptionPane.showMessageDialog(this, "Failed");

            }
        
       
        } catch (SQLException ex) {
            Logger.getLogger(StudentReg.class.getName()).log(Level.SEVERE, null, ex);
        }    }//GEN-LAST:event_jButton1ActionPerformed

