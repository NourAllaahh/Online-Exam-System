    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
           try {
              
        DefaultTableModel d =(DefaultTableModel)jTable1.getModel();
        int selectIndex = jTable1.getSelectedRow();
            String deptCode= deptcodetxt.getText();
            String deptName = deptnametxt.getText();
            String id =d.getValueAt(selectIndex, 0).toString();

            pst = con.prepareStatement("update department set DeptCode=?,DeptName=? where dept_id =?");
            pst.setString(1, deptCode);
            pst.setString(2, deptName);
            pst.setString(3, id);
            
            int P =   pst.executeUpdate();

            if (P==1)
            {
                JOptionPane.showMessageDialog(this, "Edited");

                deptcodetxt.setText("");
                deptnametxt.setText("");
                 deptcodetxt.requestFocus();
                Load();
            }
            else{
                JOptionPane.showMessageDialog(this, "Failed");

            }

        } catch (SQLException ex) {
            Logger.getLogger(StudentReg.class.getName()).log(Level.SEVERE, null, ex);
        }
   
    }//GEN-LAST:event_jButton2ActionPerformed

